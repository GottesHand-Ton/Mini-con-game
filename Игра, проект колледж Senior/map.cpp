void map::clearCell(int x, int y) {
    if (x >= 0 && x < height && y >= 0 && y < width) {
        if (jey[x][y] == 'C' || jey[x][y] == 'N') {
            jey[x][y] = '.';
        }
    }
}

void map::paint(const gg& player) {
    int ggx = player.getx();
    int ggy = player.gety();

    for (int i = 0; i < height; ++i) {
        for (int j = 0; j < width; ++j) {
            if (i == ggx && j == ggy) {
                cout << "@ ";
            }
            else {
                cout << jey[i][j] << " ";
            }
        }
        cout << endl;
    }
}